public class ex6 { //DataTypeDemo
    public static void main(String[] args) {

        int age = 20;
        float salary = 25000.50f;
        double pi = 3.14159265359;
        char grade = 'A';
        boolean isJavaEasy = true;

        System.out.println("Integer value: " + age);
        System.out.println("Float value: " + salary);
        System.out.println("Double value: " + pi);
        System.out.println("Character value: " + grade);
        System.out.println("Boolean value: " + isJavaEasy);
    }
}